function processContent(content) {
    const textRegex = /["']?text["']?:\s*(["'])([\s\S]*?)\1/g; // use \s\S for multiline or any char
    
    let modified = content.replace(textRegex, (match, quote, innerText) => {
        if (!/[\u4e00-\u9fa5]/.test(innerText)) {
            // English text, leave it alone.
            return match;
        }
        
        let cleaned = innerText;
        
        // 1. replace English 'he' / 'He' / 'HE' with '他'
        cleaned = cleaned.replace(/\bhe\b/gi, '他');
        
        // 2. remove things in parenthesis (full width and half width)
        cleaned = cleaned.replace(/[（\(][^）\)]+[）\)]/g, '');
        
        // 3. remove standalone digits
        cleaned = cleaned.replace(/[0-9]+/g, '');
        
        // clean up multiple spaces created by removals
        cleaned = cleaned.replace(/\s{2,}/g, ' ').trim();
        
        // 4. Apply manual chunk fixes
        for (let fix of CHUNK_FIXES) {
            // We use standard replace. Because of previous string manipulation, 
            // the text should exactly match except maybe whitespace.
            // Let's do a simple replace, since we cleaned it nicely.
            cleaned = cleaned.replace(fix.find, fix.replace);
        }
        
        // Safety check if innerText hasn't changed
        if (cleaned === innerText) return match;
        
        return match.replace(innerText, cleaned);
    });
    return modified;
}

const files = ['../src/verses.js', '../src/verses_psalms.js'];

for (let file of files) {
    let p = path.join(__dirname, file);
    if (fs.existsSync(p)) {
        let content = fs.readFileSync(p, 'utf8');
        let newContent = processContent(content);
        if (content !== newContent) {
            fs.writeFileSync(p, newContent, 'utf8');
            console.log(`Updated ${file}`);
        }
    }
}
